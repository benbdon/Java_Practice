/* 
   Compute the distance to from 
   a wall using the time for an echo
*/     
class SoundEcho {     
  public static void main(String args[]) {     
    double dist; 
 
    dist = 7.2 * 1100 / 2; 
 
    System.out.println("The wall is " + dist +  
                       " feet away.");  
    
  }     
}